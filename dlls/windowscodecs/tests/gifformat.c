static void test_gif_full_table(void)
{
    IWICBitmapDecoder *decoder;
    IWICBitmapFrameDecode *frame;
    UINT count = 0, width = 0, height = 0;
    BYTE buf[64 * 64];
    WICRect rc = { 0, 0, 64, 64 };
    HRESULT hr;

    decoder = create_decoder(gif_full_table, sizeof(gif_full_table));
    ok(decoder != 0, "Failed to load GIF image data\n");
    if (!decoder) return;

    hr = IWICBitmapDecoder_GetFrameCount(decoder, &count);
    ok(hr == S_OK, "GetFrameCount error %#lx\n", hr);
    ok(count == 1, "expected 1, got %u\n", count);

    hr = IWICBitmapDecoder_GetFrame(decoder, 0, &frame);
    ok(hr == S_OK, "GetFrame error %#lx\n", hr);

    hr = IWICBitmapFrameDecode_GetSize(frame, &width, &height);
    ok(hr == S_OK, "GetSize error %#lx\n", hr);
    ok(width == 64 && height == 64, "expected 64x64, got %ux%u\n", width, height);

    hr = IWICBitmapFrameDecode_CopyPixels(frame, &rc, 64, sizeof(buf), buf);
    ok(hr == S_OK, "CopyPixels error %#lx\n", hr);

    IWICBitmapFrameDecode_Release(frame);
    IWICBitmapDecoder_Release(decoder);
}

